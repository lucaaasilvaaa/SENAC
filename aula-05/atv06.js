var fruta = 4

console.log("Escolha uma fruta: ")
switch (fruta) {
    case 1: 
        console.log("Banana")
        break
    case 2:
        console.log("Maçã")
        break
    case 3: 
        console.log("Melancia")
        break
    case 4:
        console.log("Abacaxi")
        break
    default:
        console.log("Opção inválida! Informe um número entre 1 e 4")
}