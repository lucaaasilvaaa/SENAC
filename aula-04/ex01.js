//Estrutura Switch Case

// Declaração das variáveis
var opc = 1

//Iniciando a estrutura Switch Case
switch (opc) {
    case 1:
        console.log("Bom dia")
        break
    case 2:
        console.log("Boa tarde")
        break
    case 3:
        console.log("Boa noite")
        break
    default:
        console.log("Opção inválida! Informe 1, 2 ou 3.")
}